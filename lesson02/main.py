# Для этой задачи я рекомендую использовать императивный стиль программирования, 
# так как нам нужно последовательно выполнять операции и выводить результаты на экран.
# В этом примере мы используем два вложенных цикла for для создания таблицы умножения. 
# Внешний цикл перебирает числа от 1 до n, а внутренний цикл перебирает 
# числа от 1 до 9 (так как мы выводим только результаты до 9). 
# Мы вычисляем произведение каждой пары чисел и выводим его на экран в формате "число1 * число2 = результат".

def multiplication_table(n):
    for i in range(1, n+1):
        for j in range(1, 10):
            result = i * j
            print(f"{i} * {j} = {result}")

n = 5
multiplication_table(n)